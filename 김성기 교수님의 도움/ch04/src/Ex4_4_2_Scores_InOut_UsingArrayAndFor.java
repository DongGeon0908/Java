class Ex4_4_2_Scores_InOut_UsingArrayAndFor {
	public static void main(String[] args) { 

		// 점수 10개 저장할 배열변수 선언하고 배열 객체를 배열변수 scores에 저장함
		int[] scores = new int[10];   // 배열변수 선언은 항상 이렇게 할 것!!!

		// 점수 10개 입력하여 배열변수의 인덱스 0부터 9까지의 원소에 저장
		System.out.println("\n  ** 점수 10개 입력 **");     // 입력 타이틀 출력 
		System.out.print("  o 점수 10개 입력하시오 > ");    // 프롬프트 메시지 출력 
		
		for (int i = 0; i < 10; i++)
			scores[i] = SkScanner.getInt();   // 정수 값 입력하여 배열 원소 scores[i]에 저장

		// 입력된 점수 10개를 모두 출력 
		System.out.print("\n  ** 입력된 점수 10개 **\n  * ");   // 출력 타이틀 출력
		for (int i = 0; i < 10; i++)
			System.out.print(scores[i] + " ");
	}
}

