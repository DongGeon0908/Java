/**
 * [ CharacterType ]: นฎภฺ ภิทยวฯฟฉ นฎภฺ มพท๙ ฦวบฐวฯฟฉ รโทย
 *  
 *   o วมทฮฑืทกนึ ฝบลธภฯ 
 *     1) main() ธผาตๅทฮธธ ลฌทกฝบ ฑธผบ
 *     2) นฎภฺ ภิทยวฯฐํ ม฿รธตศ if นฎ ป็ฟ๋วฯฟฉ นฎภฺ มพท๙ ฦวบฐวิ
 *        
 *   o ฦวบฐวา นฎภฺ มพท๙
 *     - ฟตพ๎ ด๋นฎภฺ ภฺภฝ, ธ๐ภฝ
 *     - ฟตพ๎ ผานฎภฺ ภฺภฝ, ธ๐ภฝ
 *     - ผภฺ ต๐ม๖ฦฎ
 *     - วัฑ ภฺภฝ, ธ๐ภฝ, วัฑ ฑภฺ
 *     - ฑโลธ นฎภฺ
 *      
 *   o ม฿รธตศ if นฎ ป็ฟ๋วฯฟฉ นฎภฺ มพท๙ ฦวดวฯฐํ ฐแฐ๚ รโทย   
 *    
 *   o นฎภฺ บฏผ๖ฟก นฎภฺ ภิทย
 *     - SkScanner.getCharWithPrompt(วมทาวมฦฎ_นฎภฺฟญ): 
 *           วมทาวมฦฎ นฎภฺฟญ รโทยวั ศฤ นฎภฺ วฯณช ภิทยวฯดย ธผาตๅ
 *           
 *     - char c = SkScanner.getCharWithPrompt("  * มพท๙ ศฎภฮวา นฎภฺ > ");
 */

class Ex4_2_4_CharacterType_UsingIf {
	public static void main(String[] args) {
		// นฎภฺ มพท๙บฐ ธํฤช ภ๚ภๅวฯดย นฎภฺฟญ น่ฟญ
		String[] ctypeNames = { "ฟตด๋นฎภฺ ธ๐ภฝ", "ฟตด๋นฎภฺ ภฺภฝ", 
		                    	"ฟตผานฎภฺ ธ๐ภฝ", "ฟตผานฎภฺ ภฺภฝ",
		                    	"วัฑ ภฺภฝ", "วัฑ ธ๐ภฝ", "วัฑ ฑภฺ",
		                    	"ผภฺ ต๐ม๖ฦฎ",
		                    	"ฑโลธ นฎภฺ"
		};

		System.out.println("\n  ** นฎภฺ มพท๙ ศฎภฮวฯฑโ **\n");
	
    	char c = SkScanner.getCharWithPrompt("  o มพท๙ ศฎภฮวา นฎภฺ > ");
	
    	int ctype;  // 0บฮลอ 8ฑ๎ม๖ นฎภฺ มพท๙ ณชลธณปดย น๘ศฃ ภ๚ภๅวา บฏผ๖

		// ม฿รธตศ if นฎภว ฟน: นฎภฺ มพท๙ ศฎภฮวฯฟฉ ctypeฟก มพท๙ น๘ศฃ ภ๚ภๅ
		if (c >= 'A' && c <= 'Z') {           // 'A'บฮลอ 'Z'ฑ๎ม๖ดย ฟตด๋นฎภฺ
 			if (c=='A' || c=='E' || c=='I' || c=='O' || c=='U')  
				ctype = 0;                    //    ฟตด๋นฎภฺ ธ๐ภฝภว มพท๙ น๘ศฃ: 0
			else
				ctype = 1;                    //    ฟตด๋นฎภฺ ภฺภฝภว มพท๙ น๘ศฃ: 1
		} else if (c >= 'a' && c <= 'z') {    // 'a'บฮลอ 'z'ฑ๎ม๖ดย ฟตผานฎภฺ
 			if (c=='a' || c=='e' || c=='i' || c=='o' || c=='u')  
				ctype = 2;                    //    ฟตผานฎภฺ ธ๐ภฝภว มพท๙ น๘ศฃ: 2
			else
				ctype = 3;                    //    ฟตผานฎภฺ ภฺภฝภว มพท๙ น๘ศฃ: 3
		} else if (c >= 'คก' && c <= 'คพ')     // 'คก'ภบ วัฑ ภฺภฝ ม฿ รึผา, 'คพ'ภบ รึด๋
			ctype = 4;	                      //     วัฑ ภฺภฝภว มพท๙ น๘ศฃ: 4
		else if (c >= 'คฟ' && c <= 'คำ')       // 'คฟ'ดย วัฑ ธ๐ภฝ ม฿ รึผา, 'คำ'ดย รึด๋
			ctype = 5;	                      //     วัฑ ธ๐ภฝภว มพท๙ น๘ศฃ: 6 
		else if (c >= 'ฐก' && c <= 'ฦR')       // 'ฐก'ดย วัฑ ฑภฺ ม฿ รึผา, 'ฦR'ภบ รึด๋
			ctype = 6;	                      //     วัฑ ฑภฺภว มพท๙ น๘ศฃ: 6  
		else if (c >= '0' && c <= '9')        // '0'บฮลอ '9'ฑ๎ม๖ดย ต๐ม๖ฦฎ
			ctype = 7;                        //     ผภฺ ต๐ม๖ฦฎภว มพท๙ น๘ศฃ: 7 
		else 
			ctype = 8;                        //     ฑโลธ นฎภฺ มพท๙ภว น๘ศฃ: 8 

		System.out.println("  * ภิทย นฎภฺ '" + c + "'(มคผ๖ฐช: " + (int) c +")ภว มพท๙: "	+ ctypeNames[ctype]);	
		
		SkScanner.getChar(); SkScanner.getChar();  // !! ฟฃลอลฐธฆ ดญทถภป ถง ภิทยตวดย นฎภฺธฆ มฆฐลวฯฑโ ภงวุ รทฐกตศ นฎภๅต้
		
		main(args);  // main() ธผาตๅ นบนวุ ผ๖วเฝรลฐดย ฐกภๅ ฝฌฟ๎ นๆน(ฒภ พหพฦตั ฐอ!!!!!!)
	}
}

