/*
 * [ PrintLikeJava_10Times_UsingFor ]:  "I like Java "를 10번 출력하기
 *  
 *    o 스타일 2: 반복문 사용하여 아주 쉽게 작성하기
 *     
 *    o 작업 반복의 방법
 *      1) 직접 반복 문장을 회수만큼 나열하여 작업을 반복함
 *         - 반복된 내용을 직접 하나하나 도장 찍듯이 작성
 *         - 회수가 많아질 경우 정확한 회수만큼 작성하기 어려움
 *         - 회수를 변경하기 어려움
 *         - 프로그램의 길이가 길어짐
 *      
 *      2) 반복문 이용
 *         - 반복 제어를 이용하여 원하는 만큼 작업을 반복시킴
 *         - 반복 회수가 많아져도 프로그램이 길어지지 않음
 *         - 프로그램이 간결해짐
 *         - 반복 회수의 변경이 용이함 
 *         
 *    o 반복문(repetition statement)
 *      - 똑같은 문장들 또는 유사한 문장들이 반복적으로 나열되는 경우 
 *        이를 하나의 문장으로 통합적으로 구성하여 반복시킴
 *      - for 문, while 문, do-while문 등의 반복문      
 *      
 *    o for 문
 *      - 반복 제어변수를 이용하여 반복 회수를 제어함
 *      - 반복 제어변수는 for 문 외부 또는 내부에서 선언 가능
 *      - for 문 외부에서 선언한 반복 제어변수는 for 문 내와 for 문이 종료된 후에도 사용 가능함 
 *      - for 문 내부에서 선언한 반복 제어변수는 for 문 내에서만 사용 가능함 
 */

class Ex4_04_2_PrintLikeJava_10Times_UsingFor {
	public static void main(String[] args)  {
		System.out.print("\n  **** Print \"I like Java\" 10 times using for statement ****\n");

		int i;  // for 문에서의 반복 제어변수

		for (i = 0; i < 10; i++)               // i가 0부터 10보다 작을 동안 1씩 증가하면서 반복
			System.out.print("I like Java ");  //     반복 수행할 문장

		System.out.println("\n\n  * for 문이 종료된 후 i = " + i);  // 반복 제어변수 i를 for 문 외부에서 선언했기 대문에 
		                                                          // for 문이 끝난 후에도 사용가능함
	}
}

